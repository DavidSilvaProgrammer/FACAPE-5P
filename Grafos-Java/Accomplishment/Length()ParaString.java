public class Main {
    public static void main(String[] args) {
        // Definindo uma string com um caractere de nova linha
        String example = "Hello\nWorld";
        
        // Exibir o comprimento da string
        System.out.println("Comprimento da string: " + example.length());
        
        // Exibir os caracteres e seus índices
        for (int i = 0; i < example.length(); i++) {
            System.out.println("Índice " + i + ": " + example.charAt(i));
            
            
        System.out.println(example.length());
        }
    }
}
